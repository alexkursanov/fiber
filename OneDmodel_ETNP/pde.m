function [Yqn,V_1]=pde(t0,ts,x0,xn,n,D,Yq,Y0)
%--------------------------------------
global jj;
jj=1;
[~,ZZ]=ode15s(@TNNPE,[t0 ts],Y0);
%--------------------------------------
s=2;
x=x0:(xn-x0)/(n):xn; dx=x(2)-x(1);
t=t0:(ts-t0)/(s-1):ts; dt=t(2)-t(1);
%--------------------------------------
U=zeros(n,s);
U(:,1)=Yq(:,13);
G=(D*dt/1000)/(dx)^2;
a=zeros(1,n);
b=zeros(1,n);

%--------------------------------------
    a(1)=2*G/(1+2*G);
    b(1)=ZZ(end,13)/(1+2*G);
    for i=2:n
        a(i)=G/(-G*a(i-1)+2*G+1);
        b(i)=(G*b(i-1)+U(i,1))/(-G*a(i-1)+2*G+1);
    end
    U(n,2)=(b(n)+a(n)*b(n-1))/(1-a(n)*a(n-1));
    for i=n-1:-1:1
        U(i,2)= a(i)*U(i+1,2)+b(i);
    end
%--------------------------------------

 Yqn=U(1:n,end);
 V_1=ZZ(end,:);
%--------------------------------------